export type TextTemplateCategory =
  | 'heading'
  | 'quote'
  | 'business'
  | 'seasonal'
  | 'announcement';

export interface TextTemplateLayer {
  text: string;
  fontFamily: string;
  fontSize: number;
  fontStyle?: 'normal' | 'bold' | 'italic' | 'bold italic';
  fill: string;
  align?: 'left' | 'center' | 'right';
  x: number;
  y: number;
  width: number;
  letterSpacing?: number;
  lineHeight?: number;
}

export interface TextTemplatePreset {
  id: string;
  name: string;
  category: TextTemplateCategory;
  layers: TextTemplateLayer[];
  previewBg: string;
  referenceWidth: number;
  referenceHeight: number;
}

